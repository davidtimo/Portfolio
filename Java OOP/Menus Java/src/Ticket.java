
public class Ticket {
    private String gender;
    private String name;
    private int tix;
    private String id;
    private long price;
    private String tixPay;

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTix() {
        return tix;
    }

    public void setTix(int tix) {
        this.tix = tix;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public long getPrice() {
        return price;
    }

    public void setPrice(long price) {
        this.price = price;
    }

    public String getTixPay() {
        return tixPay;
    }

    public void setTixPay(String tixPay) {
        this.tixPay = tixPay;
    }
}
